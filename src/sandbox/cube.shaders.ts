import { ShaderSource } from "../shader";

export const vertex = ShaderSource.vertex`
in vec3 aPosition;
in vec4 aColor;

uniform mat4 uMVP;
uniform mat4 uTransform;

out vec4 vColor;

void main() {
  gl_Position = uMVP * uTransform * vec4(aPosition, 1.0);
  vColor = aColor;
}
`;

export const fragment = ShaderSource.fragment`
precision mediump float;

in vec4 vColor;

out vec4 color;

void main() {
  color = vColor;
}
`;

